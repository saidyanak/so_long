#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		s++;
	}
	if ((char)c == '\0')
		return ((char *)(s + i));
	return (NULL);
}
